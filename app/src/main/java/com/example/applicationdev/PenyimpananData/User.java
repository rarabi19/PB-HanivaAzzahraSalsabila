package com.example.applicationdev.PenyimpananData;

public class User {
    public String nama, email, nim, jurusan, universitas;

    public User() {
    }

    public User(String nama, String email, String nim, String jurusan, String universitas) {
        this.nama = nama;
        this.email = email;
        this.nim = nim;
        this.jurusan = jurusan;
        this.universitas = universitas;
    }
}
